const recentEmojis = ['😀', '👍', '❤️', '😂', '🎉', '🔥', '💯', '✨'];

const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, className }) => {
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedCategory, setSelectedCategory] = useState('smileys');

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
  };

  const filteredEmojis = searchTerm
    ? Object.values(emojiCategories)
        .flatMap(category => category.emojis)
        .filter(emoji => emoji.includes(searchTerm))
    : emojiCategories[selectedCategory as keyof typeof emojiCategories]?.emojis || [];

  return (
    <Popover>
      <PopoverTrigger asChild>
        <Button variant="ghost" size="icon" className={cn("flex-shrink-0", className)}>
          <Smile className="h-4 w-4" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-80 p-0" align="end">
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              placeholder="Search emojis..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="pl-10"
            />
          </div>
        </div>

        {!searchTerm && (
          <>
            {/* Recent Emojis */}
            <div className="p-3 border-b">
              <h4 className="text-sm font-medium mb-2">Recently Used</h4>
              <div className="grid grid-cols-8 gap-1">
                {recentEmojis.map((emoji, index) => (
                  <Button
                    key={index}
                    variant="ghost"
                    size="sm"
                    className="h-8 w-8 p-0 text-lg hover:bg-accent"
                    onClick={() => handleEmojiClick(emoji)}
                  >
                    {emoji}
                  </Button>
                ))}
              </div>
            </div>

            {/* Category Tabs */}
            <Tabs value={selectedCategory} onValueChange={setSelectedCategory}>
              <TabsList className="grid w-full grid-cols-6 h-8 mx-3 mt-2">
                <TabsTrigger value="smileys" className="text-xs px-1">😀</TabsTrigger>
                <TabsTrigger value="people" className="text-xs px-1">👋</TabsTrigger>
                <TabsTrigger value="nature" className="text-xs px-1">🐶</TabsTrigger>
                <TabsTrigger value="food" className="text-xs px-1">🍎</TabsTrigger>
                <TabsTrigger value="activity" className="text-xs px-1">⚽</TabsTrigger>
                <TabsTrigger value="objects" className="text-xs px-1">⌚</TabsTrigger>
              </TabsList>
            </Tabs>
          </>
        )}

        {/* Emoji Grid */}
        <ScrollArea className="h-64">
          <div className="p-3">
            <div className="grid grid-cols-8 gap-1">
              {filteredEmojis.map((emoji, index) => (
                <Button
                  key={index}
                  variant="ghost"
                  size="sm"
                  className="h-8 w-8 p-0 text-lg hover:bg-accent"
                  onClick={() => handleEmojiClick(emoji)}
                >
                  {emoji}
                </Button>
              ))}
            </div>
            {filteredEmojis.length === 0 && searchTerm && (
              <div className="text-center py-4 text-muted-foreground text-sm">
                No emojis found for "{searchTerm}"
              </div>
            )}
          </div>
        </ScrollArea>
      </PopoverContent>
    </Popover>
  );
};

export default EmojiPicker;